@dataclass
class ParagraphMatch:
    """–†–µ–∑—É–ª—å—Ç–∞—Ç —Å–æ–ø–æ—Å—Ç–∞–≤–ª–µ–Ω–∏—è –ø–∞—Ä–∞–≥—Ä–∞—Ñ–∞"""
    number: int
    title: str
    score: int = 0
    matched_keywords: list = field(default_factory=list)
    categories: list = field(default_factory=list)


def find_paragraph(text: str, top_n: int = 3) -> list[tuple[int, str, int]]:
    """
    –û–ø—Ä–µ–¥–µ–ª—è–µ—Ç –ø–∞—Ä–∞–≥—Ä–∞—Ñ(—ã) –ø–æ —Ç–µ–∫—Å—Ç—É –∑–∞–¥–∞—á–∏ (–ø—Ä–æ—Å—Ç–æ–π —Ä–µ–∂–∏–º).

    Args:
        text: –¢–µ–∫—Å—Ç –∑–∞–¥–∞—á–∏ –∏–ª–∏ –∫–ª—é—á–µ–≤—ã–µ —Å–ª–æ–≤–∞
        top_n: –ö–æ–ª–∏—á–µ—Å—Ç–≤–æ –Ω–∞–∏–±–æ–ª–µ–µ –≤–µ—Ä–æ—è—Ç–Ω—ã—Ö –ø–∞—Ä–∞–≥—Ä–∞—Ñ–æ–≤ –¥–ª—è –≤–æ–∑–≤—Ä–∞—Ç–∞

    Returns:
        –°–ø–∏—Å–æ–∫ –∫–æ—Ä—Ç–µ–∂–µ–π (–Ω–æ–º–µ—Ä_–ø–∞—Ä–∞–≥—Ä–∞—Ñ–∞, –Ω–∞–∑–≤–∞–Ω–∏–µ, –∫–æ–ª–∏—á–µ—Å—Ç–≤–æ_—Å–æ–≤–ø–∞–¥–µ–Ω–∏–π)
    """
    text_lower = text.lower()
    scores = []

    for para_num, para_data in PARAGRAPH_KEYWORDS.items():
        score = 0
        for keyword in para_data["keywords"]:
            if keyword.lower() in text_lower:
                score += len(keyword.split())

        if score > 0:
            scores.append((para_num, para_data["title"], score))

    scores.sort(key=lambda x: x[2], reverse=True)
    return scores[:top_n]


def analyze_problem(text: str, threshold: int = 1) -> dict:
    """
    –ü–æ–ª–Ω—ã–π –∞–Ω–∞–ª–∏–∑ —É—Å–ª–æ–≤–∏—è –∑–∞–¥–∞—á–∏ ‚Äî –æ–ø—Ä–µ–¥–µ–ª—è–µ—Ç –í–°–ï –ø–∞—Ä–∞–≥—Ä–∞—Ñ—ã, –∫–æ—Ç–æ—Ä—ã–µ –ø–æ–Ω–∞–¥–æ–±—è—Ç—Å—è.

    Args:
        text: –ü–æ–ª–Ω–æ–µ —É—Å–ª–æ–≤–∏–µ –∑–∞–¥–∞—á–∏
        threshold: –ú–∏–Ω–∏–º–∞–ª—å–Ω—ã–π score –¥–ª—è –≤–∫–ª—é—á–µ–Ω–∏—è –ø–∞—Ä–∞–≥—Ä–∞—Ñ–∞

    Returns:
        –°–ª–æ–≤–∞—Ä—å —Å —Ä–µ–∑—É–ª—å—Ç–∞—Ç–∞–º–∏ –∞–Ω–∞–ª–∏–∑–∞:
        - main: –æ—Å–Ω–æ–≤–Ω—ã–µ –ø–∞—Ä–∞–≥—Ä–∞—Ñ—ã (–≤—ã—Å–æ–∫–∏–π score)
        - auxiliary: –≤—Å–ø–æ–º–æ–≥–∞—Ç–µ–ª—å–Ω—ã–µ –ø–∞—Ä–∞–≥—Ä–∞—Ñ—ã (–Ω–∏–∑–∫–∏–π score)
        - categories: –Ω–∞–π–¥–µ–Ω–Ω—ã–µ –∫–æ–Ω—Ü–µ–ø—Ü–∏–∏ –ø–æ –∫–∞—Ç–µ–≥–æ—Ä–∏—è–º
        - all: –≤—Å–µ –Ω–∞–π–¥–µ–Ω–Ω—ã–µ –ø–∞—Ä–∞–≥—Ä–∞—Ñ—ã
    """
    text_lower = text.lower()
    paragraph_scores: dict[int, ParagraphMatch] = {}

    # 1. –ü–æ–∏—Å–∫ –ø–æ –æ—Å–Ω–æ–≤–Ω—ã–º –∫–ª—é—á–µ–≤—ã–º —Å–ª–æ–≤–∞–º
    for para_num, para_data in PARAGRAPH_KEYWORDS.items():
        matched = []
        score = 0
        for keyword in para_data["keywords"]:
            if keyword.lower() in text_lower:
                matched.append(keyword)
                score += len(keyword.split())

        if score > 0:
            paragraph_scores[para_num] = ParagraphMatch(
                number=para_num,
                title=para_data["title"],
                score=score,
                matched_keywords=matched,
                categories=[]
            )

    # 2. –î–æ–ø–æ–ª–Ω–∏—Ç–µ–ª—å–Ω—ã–π –∞–Ω–∞–ª–∏–∑ –ø–æ –∫–∞—Ç–µ–≥–æ—Ä–∏—è–º
    category_matches: dict[str, list[str]] = {}

    for category, concepts in CONCEPT_CATEGORIES.items():
        category_matches[category] = []
        for concept, paragraphs in concepts.items():
            if concept.lower() in text_lower:
                category_matches[category].append(concept)
                for para_num in paragraphs:
                    if para_num in paragraph_scores:
                        if category not in paragraph_scores[para_num].categories:
                            paragraph_scores[para_num].categories.append(category)
                        paragraph_scores[para_num].score += 1
                    else:
                        paragraph_scores[para_num] = ParagraphMatch(
                            number=para_num,
                            title=PARAGRAPH_KEYWORDS[para_num]["title"],
                            score=1,
                            matched_keywords=[concept],
                            categories=[category]
                        )

    # 3. –§–∏–ª—å—Ç—Ä–∞—Ü–∏—è –∏ —Å–æ—Ä—Ç–∏—Ä–æ–≤–∫–∞
    results = [p for p in paragraph_scores.values() if p.score >= threshold]
    results.sort(key=lambda x: (-x.score, x.number))

    # 4. –†–∞–∑–¥–µ–ª–µ–Ω–∏–µ –Ω–∞ –æ—Å–Ω–æ–≤–Ω—ã–µ –∏ –≤—Å–ø–æ–º–æ–≥–∞—Ç–µ–ª—å–Ω—ã–µ
    main_paragraphs = [r for r in results if r.score >= 3]
    auxiliary_paragraphs = [r for r in results if 1 <= r.score < 3]

    return {
        "main": main_paragraphs,
        "auxiliary": auxiliary_paragraphs,
        "categories": category_matches,
        "all": results
    }


